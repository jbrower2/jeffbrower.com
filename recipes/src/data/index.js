function nameFromMarkdown(md) {
  const firstLine = md.split("\n", 1)[0];
  return firstLine.replace(/^#\s*/, "").trim();
}

export default [...notShown, ...shown].sort().map((slug) => {
  const markdown = require(`./${slug}.md`);
  return {
    slug,
    name: nameFromMarkdown(markdown),
    show: shown.has(slug),
    markdown,
  };
});
