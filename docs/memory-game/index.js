  const wordCount = (width * height) / 2;
  const wordList = [];

  // fill list of words
  while (wordList.length < wordCount) {
    const word = sightWords[Math.floor(Math.random() * maxSightWord)];
    if (wordList.indexOf(word) === -1) {
      wordList.push(word);
    }
  }

  // duplicate list
  wordList.push(...wordList);

  // shuffle
  for (let i = 0; i < wordList.length; i++) {
    const j = Math.floor(Math.random() * wordList.length);
    if (i !== j) {
      const temp = wordList[i];
      wordList[i] = wordList[j];
      wordList[j] = temp;
    }
  }

  const table = document.createElement("table");
  document.body.appendChild(table);
  const flippedCells = {};

  function allMatched() {
    if (Object.keys(flippedCells).length < wordCount) {
      return false;
    }

    for (const flipped of Object.values(flippedCells)) {
      if (!flipped.matched) {
        return false;
      }
    }

    return true;
  }

  function drawEmoji() {
    const enabledEmojis = [];
    Object.values(emojis).forEach((a) => {
      Object.values(a).forEach((b) => {
        b.enabled && enabledEmojis.push(...b);
      });
    });
    for (const badEmoji of ["👙"]) {
      const index = enabledEmojis.indexOf(badEmoji);
      if (index !== -1) enabledEmojis.splice(index, 1);
    }

    const emojiList = [];
    const getNewEmoji = () => {
      if (!emojiList.length) {
        console.log("Refreshing emoji list...");
        emojiList.push(...enabledEmojis);
        for (let i = 0; i < emojiList.length; i++) {
          const j = Math.floor(Math.random() * emojiList.length);
          if (i === j) continue;
          const temp = emojiList[i];
          emojiList[i] = emojiList[j];
          emojiList[j] = temp;
        }
      }
      return emojiList.pop();
    };

    const reset = (emoji) => {
      emoji.innerText = getNewEmoji();
      emoji.style.transition = "initial";
      emoji.style.left = Math.random() * 100 + "vw";
      emoji.style.top = "-2em";
      const x = Math.random();
      emoji.style.zIndex = Math.floor(x * 1000);
      emoji.style.fontSize = x * 100 + 50 + "px";
      const time = Math.floor(2500 * (2 - x));
      setTimeout(() => {
        emoji.style.transition =
          "top " + time + "ms cubic-bezier(.667, 0, 1, 1) 0s";
        emoji.style.top = "100vh";
      }, 0);
      setTimeout(() => reset(emoji), time);
    };

    for (let i = 0; i < 150; i++) {
      const emoji = document.createElement("div");
      emoji.classList.add("emoji");
      document.body.appendChild(emoji);
      setTimeout(() => reset(emoji), Math.floor(Math.random() * 5000));
    }
  }

  for (let y = 0, i = 0; y < height; y++) {
    const tr = document.createElement("tr");
    table.appendChild(tr);

    for (let x = 0; x < width; x++, i++) {
      const word = wordList[i];

      const td = document.createElement("td");
      tr.appendChild(td);

      const card = document.createElement("div");
      td.appendChild(card);
      card.classList.add("card");
      card.onclick = function () {
        if (card.matched) return;
        card.classList.toggle("flipped");
        if (card.classList.contains("flipped")) {
          const flipped = flippedCells[word];
          if (flipped) {
            flipped.classList.toggle("matched");
            flipped.matched = true;
            card.classList.toggle("matched");
            card.matched = true;
            if (allMatched()) {
              drawEmoji();
            }
          } else {
            flippedCells[word] = card;
          }
        } else {
          delete flippedCells[word];
        }
      };

      const front = document.createElement("div");
      card.appendChild(front);
      front.classList.add("front");

      const frontText = document.createElement("div");
      front.appendChild(frontText);
      frontText.innerText = i + 1;

      const back = document.createElement("div");
      card.appendChild(back);
      back.classList.add("back");

      const backText = document.createElement("div");
      back.appendChild(backText);
      backText.innerText = word;
    }
  }
